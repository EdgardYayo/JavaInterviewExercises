package Boolean;

public class Boolean {
    public static void main(String[] args) {
        boolean myTrueBoolean = true;
        boolean myFalseBoolean = false;

        // The best practice of naming the variables of booleans are the following
        boolean isCustomerOlderEnough = false;
        boolean hasHomework = true;
        boolean isLearningJava = true;
        boolean hasChildren = false;

        // As you can see boolean variables as a good practice always starts with a 'is' or 'have/has'
        // This is no forced by the compiler but instead it makes your code more readable and understandable
    }
}