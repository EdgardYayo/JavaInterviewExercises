package DataTypesPrints;

public class DataTypesPrints {

    public static void main(String[] args) {
        // Long    
        System.out.println("Long Values: (" + Long.MIN_VALUE + " to " + Long.MAX_VALUE + ")");
        System.out.println("The size of Long is " + Long.SIZE);
        // Integer
        System.out.println("Integer Values: (" + Integer.MIN_VALUE + " to " + Integer.MAX_VALUE + ")");
        System.out.println("The size of Integer is " + Integer.SIZE);
        // Short
        System.out.println("Short Values: (" + Short.MIN_VALUE + " to " + Short.MAX_VALUE + ")");
        System.out.println("The size of Short is " + Short.SIZE);
        // Byte
        System.out.println("Byte Values: (" + Byte.MIN_VALUE + " to " + Byte.MAX_VALUE + ")");
        System.out.println("The size of Byte is " + Byte.SIZE);
        // Float
        System.out.println("Float Values: (" + Float.MIN_VALUE + " to " + Float.MAX_VALUE + ")");
        System.out.println("The size of Float is " + Float.SIZE);
        // Double
        System.out.println("Double Values: (" + Double.MIN_VALUE + " to " + Double.MAX_VALUE + ")");
        System.out.println("The size of Double is " + Double.SIZE);
    }
}